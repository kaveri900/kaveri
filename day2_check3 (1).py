# Hardcoded variables
item_name = "Laptop"      # String
quantity = 2              # Integer
price = 499.99            # Float
in_stock = True           # Boolean

# Display the item profile using string formatting
print("Item Profile")
print("------------")
print(f"Item Name : {item_name}")
print(f"Quantity  : {quantity}")
print(f"Price     : ${price}")
print(f"In Stock  : {in_stock}")

# Show data types
print("\nData Types:")
print("item_name:", type(item_name))
print("quantity:", type(quantity))
print("price:", type(price))
print("in_stock:", type(in_stock))
